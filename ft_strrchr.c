char	ft_strrchr(const char *s, int c)
{
	char	*ps;

	if (c == 0)
		return (0);
	while (*s != 0)
	{
		if (*s == (char)c)
		{
			ps = (char *)s;
			s++;
		}
	}
	return (ps);
}
